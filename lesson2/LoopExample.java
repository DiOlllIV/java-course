package lesson2;

public class LoopExample {
    public static void main(String[] args) {
        // for loop
        // while loop
        // for each loop
        // do while loop

        // 1. while loop
        int a = 1;

        while (a <= 10) {
            /*System.out.println(a);*/
            a++;
        }
        /*System.out.println(100);*/

        // 2. do while loop
        int b = 20;

        do {
            System.out.println(b);
            b--;
        } while (b > 10);
    }
}
